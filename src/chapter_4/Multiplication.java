package chapter_4;

public class Multiplication {
    public static void main(String[] args) {
        for (int i = 1; i < 3; i++) {
            for (int j = 1; j <13 ; j++) {
                System.out.print("\n"+i +"* "+ j+ "="+i * j);
            }
            System.out.println();
        }
    }
}
